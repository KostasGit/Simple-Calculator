import UIKit

extension UIColor {
    struct CalculatorColors {
        static var lightGray: UIColor {
            return UIColor(red: 170/255, green: 170/255, blue: 170/255, alpha: 1.0)
        }
        
        static var darkGray: UIColor {
            return UIColor(red: 85/255, green: 85/255, blue: 85/255, alpha: 1.0)
        }
        
        static var orange: UIColor {
            return UIColor(red: 255/255, green: 122/255, blue: 0/255, alpha: 1.0)
        }
    }
}
